public class Calculator {
    void PowerOn() {
        System.out.println("전원이 켜집니다.");
    }

    int add(int a, int b) {
        int result = a + b;
        return result;
    }

    double divide(int x, int y) {
        double result = x / y;
        return result;
    }

    void PowerOff() {
        System.out.println("전원이 꺼집니다.");
    }
}
